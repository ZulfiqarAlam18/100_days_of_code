
fruits = ["Apple", "Banana", "Orange"]

fruits.append("Checku")
updated_list = fruits


print(updated_list)


# Remove method


updated_list.remove("Banana")

print(updated_list)

# Length of list

print(len(updated_list))

# Update Any value in list

updated_list[0] = "Mango"

print(updated_list)

# Insert value at specific index

updated_list.insert(1, "Grape")

print(updated_list)

# Sort list in ascending order

updated_list.sort()

print(updated_list)

# Sort list in descending order

updated_list.sort(reverse=True)

print(updated_list)

